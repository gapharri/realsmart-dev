#!/usr/bin/env bash
# Sync Laravel Cloud environment variables for RealSmart suite apps.
#
# Usage:
#   sync-cloud-env.sh <app>           # Set shared + per-app vars on one app
#   sync-cloud-env.sh --all           # All 8 suite apps
#   sync-cloud-env.sh --suite-urls    # Suite cross-link *_URL vars only
#   sync-cloud-env.sh --bootstrap     # Create ~/.config/realsmart/cloud-shared.env from smartcops
#
set -euo pipefail

export PATH="${HOME}/.composer/vendor/bin:${PATH}"

HERD_ROOT="${HERD_ROOT:-$HOME/Herd}"
OAUTH_FILE="${REALSMART_OAUTH_FILE:-$HOME/.config/realsmart/google-oauth.env}"
SHARED_FILE="${REALSMART_CLOUD_SHARED:-$HOME/.config/realsmart/cloud-shared.env}"
CLOUD_ENV="${CLOUD_ENV:-production}"
CLOUD_REGION="${CLOUD_REGION:-eu-west-2}"
DB_CLUSTER="${DB_CLUSTER:-xp-trust-db}"

SUITE_APPS=(zebidee staff-dash smaps smartbadges smartcops sos2 plan2do smartdocs)

app_display_name() {
    case "$1" in
        zebidee) echo "ZeBIDee" ;;
        staff-dash) echo "Staff Dash" ;;
        smaps) echo "SMAPS" ;;
        smartbadges) echo "SmartBadges" ;;
        smartcops) echo "SmartCoPs" ;;
        sos2) echo "SOS2" ;;
        plan2do) echo "plan2do" ;;
        smartdocs) echo "smartDocs" ;;
        *) echo "$1" ;;
    esac
}

suite_url_for_key() {
    case "$1" in
        ZEBIDEE_URL) echo "https://zebidee.laravel.cloud" ;;
        STAFF_DASH_URL) echo "https://staff-dash.laravel.cloud" ;;
        SMAPS_URL) echo "https://smaps.laravel.cloud" ;;
        SMARTBADGES_URL) echo "https://smartbadges.laravel.cloud" ;;
        SMARTCOPS_URL) echo "https://smartcops.laravel.cloud" ;;
        SOS2_URL) echo "https://sos2.laravel.cloud" ;;
        PLAN2DO_URL) echo "https://plan2do.laravel.cloud" ;;
        SMARTDOCS_URL) echo "https://smartdocs.laravel.cloud" ;;
    esac
}

cloud_url_for_app() {
    local app="$1"
    local url

    url="$(cloud environment:get "$app" --json -n 2>/dev/null | php -r '
        $d = json_decode(stream_get_contents(STDIN), true);
        echo $d["url"] ?? "";
    ' || true)"

    if [[ -n "$url" ]]; then
        echo "$url"
        return
    fi

    echo "https://${app}.laravel.cloud"
}

cloud_redirect_uri() {
    echo "$(cloud_url_for_app "$1")/auth/google/callback"
}

load_shared_secrets() {
    if [[ -f "$OAUTH_FILE" ]]; then
        # shellcheck disable=SC1090
        source "$OAUTH_FILE"
    fi

    if [[ -f "$SHARED_FILE" ]]; then
        # shellcheck disable=SC1090
        source "$SHARED_FILE"
    fi

    if [[ -z "${FLUX_USERNAME:-}" || -z "${FLUX_LICENSE_KEY:-}" || -z "${GEMINI_API_KEY:-}" ]]; then
        bootstrap_from_smartcops
    fi

    if [[ -z "${GOOGLE_CLIENT_ID:-}" || -z "${GOOGLE_CLIENT_SECRET:-}" ]]; then
        echo "GOOGLE_CLIENT_ID and GOOGLE_CLIENT_SECRET required ($OAUTH_FILE or smartcops export)" >&2
        exit 1
    fi
}

bootstrap_from_smartcops() {
    echo "Bootstrapping shared secrets from smartcops production..." >&2

    local json build_cmd flux_user flux_key gemini_key
    json="$(cloud environment:get production --json --show-sensitive -n --fields=environmentVariables 2>/dev/null || true)"

    if [[ -n "$json" ]]; then
        gemini_key="$(printf '%s' "$json" | php -r '
            $d = json_decode(stream_get_contents(STDIN), true);
            foreach ($d["environmentVariables"] ?? [] as $v) {
                if ($v["key"] === "GEMINI_API_KEY") { echo $v["value"]; exit; }
            }
        ' 2>/dev/null || true)"
        [[ -n "$gemini_key" ]] && GEMINI_API_KEY="$gemini_key"
    fi

    build_cmd="$(cloud application:get smartcops --json -n 2>/dev/null | php -r '
        $d = json_decode(stream_get_contents(STDIN), true);
        foreach ($d["environments"] ?? [] as $env) {
            if (($env["name"] ?? "") === "production") { echo $env["buildCommand"] ?? ""; exit; }
        }
    ' || true)"

    if [[ -n "$build_cmd" ]]; then
        flux_user="$(echo "$build_cmd" | sed -n 's/.*composer\.fluxui\.dev \([^ ]*\) \([^ ]*\).*/\1/p' | head -1)"
        flux_key="$(echo "$build_cmd" | sed -n 's/.*composer\.fluxui\.dev \([^ ]*\) \([^ ]*\).*/\2/p' | head -1)"
        [[ -n "$flux_user" ]] && FLUX_USERNAME="$flux_user"
        [[ -n "$flux_key" ]] && FLUX_LICENSE_KEY="$flux_key"
    fi

    mkdir -p "$(dirname "$SHARED_FILE")"
    if [[ ! -f "$SHARED_FILE" ]]; then
        cat >"$SHARED_FILE" <<EOF
# Auto-generated by sync-cloud-env.sh --bootstrap. Do not commit.
FLUX_USERNAME=${FLUX_USERNAME:-}
FLUX_LICENSE_KEY=${FLUX_LICENSE_KEY:-}
GEMINI_API_KEY=${GEMINI_API_KEY:-}
EOF
        chmod 600 "$SHARED_FILE"
        echo "Wrote $SHARED_FILE" >&2
    fi
}

set_cloud_var() {
    local app="$1" key="$2" value="$3"
    cloud environment:variables "$app" \
        --action=set \
        --key="$key" \
        --value="$value" \
        -n --force >/dev/null
}

generate_app_key() {
    local app="$1"
    (cd "$HERD_ROOT/$app" && php artisan key:generate --show 2>/dev/null)
}

app_extras() {
    local app="$1"
    local env_file="$HERD_ROOT/$app/.env.example"

    case "$app" in
        smartbadges)
            set_cloud_var "$app" "SMARTBADGES_ISSUER_NAME" "XP School Trust"
            set_cloud_var "$app" "SMARTBADGES_ISSUER_URL" "https://xptrust.org"
            set_cloud_var "$app" "SMARTBADGES_ISSUER_EMAIL" "badges@xptrust.org"
            set_cloud_var "$app" "SMARTBADGES_ISSUER_DESCRIPTION" "Open Badges issued by XP School Trust via SmartBadges."
            set_cloud_var "$app" "SMARTBADGES_ISSUER_IMAGE" "https://xptrust.org/logo.png"
            set_cloud_var "$app" "SMARTBADGES_RECIPIENT_SALT" "$(openssl rand -hex 16)"
            ;;
        zebidee|smartcops)
            set_cloud_var "$app" "GEMINI_API_KEY" "$GEMINI_API_KEY"
            ;;
        sos2)
            set_cloud_var "$app" "SOS2_ALLOWED_DOMAINS" "xptrust.org,xpschool.org,xpderby.org"
            ;;
        staff-dash)
            set_cloud_var "$app" "STAFF_DASH_ALLOWED_DOMAINS" "xptrust.org,xpschool.org,xpderby.org,xpgateshead.org,xpeast.org,carcroftschool.co.uk,ploverschool.co.uk,realsmart.co.uk"
            ;;
        smartdocs)
            set_cloud_var "$app" "SMARTDOCS_ALLOWED_DOMAINS" "xptrust.org,xpschool.org,xpderby.org,realsmart.co.uk"
            set_cloud_var "$app" "CONTENT_REPO_PATH" "/var/www/html/storage/content"
            set_cloud_var "$app" "SMARTDOCS_GIT_REMOTE" "origin"
            set_cloud_var "$app" "SMARTDOCS_DRIVE_EXPORT_FORMAT" "markdown"
            ;;
    esac
}

sync_suite_urls() {
    local app="$1"
    local keys="ZEBIDEE_URL STAFF_DASH_URL SMAPS_URL SMARTBADGES_URL SMARTCOPS_URL SOS2_URL PLAN2DO_URL SMARTDOCS_URL"
    local key url
    for key in $keys; do
        url="$(suite_url_for_key "$key")"
        set_cloud_var "$app" "$key" "$url"
    done
}

sync_app() {
    local app="$1"
    local mode="${2:-full}"
    local app_url app_name redirect app_key

    app_url="$(cloud_url_for_app "$app")"
    app_name="$(app_display_name "$app")"
    redirect="$(cloud_redirect_uri "$app")"

    if [[ "$mode" == "suite-urls" ]]; then
        sync_suite_urls "$app"
        echo "Suite URLs synced for $app"
        return
    fi

    app_key="$(generate_app_key "$app")"
    if [[ -z "$app_key" ]]; then
        echo "Failed to generate APP_KEY for $app" >&2
        exit 1
    fi

    set_cloud_var "$app" "APP_NAME" "$app_name"
    set_cloud_var "$app" "APP_ENV" "production"
    set_cloud_var "$app" "APP_DEBUG" "false"
    set_cloud_var "$app" "APP_URL" "$app_url"
    set_cloud_var "$app" "APP_KEY" "$app_key"
    set_cloud_var "$app" "GOOGLE_CLIENT_ID" "$GOOGLE_CLIENT_ID"
    set_cloud_var "$app" "GOOGLE_CLIENT_SECRET" "$GOOGLE_CLIENT_SECRET"
    set_cloud_var "$app" "GOOGLE_REDIRECT_URI" "$redirect"

    app_extras "$app"
    sync_suite_urls "$app"

    echo "Env synced for $app ($app_url)"
}

set_build_commands() {
    local app="$1"
    local build_cmd deploy_cmd smartdocs_build

    build_cmd=$'composer config http-basic.composer.fluxui.dev "$FLUX_USERNAME" "$FLUX_LICENSE_KEY"\n\ncomposer install --no-dev --no-interaction --prefer-dist --optimize-autoloader\n\nnpm ci --audit false\nnpm run build'
    deploy_cmd="php artisan migrate --force"

    if [[ "$app" == "smartdocs" ]]; then
        smartdocs_build=$'git clone --depth 1 https://github.com/gapharri/xp-trust-docs.git storage/content\n\n'
        build_cmd="${smartdocs_build}${build_cmd}"
    fi

    cloud environment:update "$app" \
        --build-command="$build_cmd" \
        --deploy-command="$deploy_cmd" \
        -n --force >/dev/null

    echo "Build/deploy commands set for $app"
}

schema_name_for_app() {
    case "$1" in
        staff-dash) echo "staff_dash" ;;
        *) echo "$1" ;;
    esac
}

attach_database() {
    local app="$1"
    local schema_id schema_name
    schema_name="$(schema_name_for_app "$app")"

    schema_id="$(cloud database:list "$DB_CLUSTER" --json -n 2>/dev/null | php -r "
        \$d = json_decode(stream_get_contents(STDIN), true);
        foreach (\$d as \$row) {
            if ((\$row['name'] ?? '') === '$schema_name') { echo \$row['id']; exit; }
        }
    " || true)"

    if [[ -z "$schema_id" ]]; then
        echo "Creating database schema $schema_name on $DB_CLUSTER..."
        schema_id="$(cloud database:create "$DB_CLUSTER" --name="$schema_name" --json -n | php -r 'echo json_decode(stream_get_contents(STDIN), true)["id"];')"
    fi

    cloud environment:update "$app" --database-id="$schema_id" -n --force >/dev/null
    echo "Database $schema_name attached to $app"
}

ensure_vanity_domain() {
    local app="$1"
    local vanity="${app}.laravel.cloud"

    if cloud domain:list "$app" --json -n 2>/dev/null | php -r "
        \$d = json_decode(stream_get_contents(STDIN), true);
        foreach (\$d as \$row) {
            if ((\$row['name'] ?? '') === '$vanity') { exit(0); }
        }
        exit(1);
    "; then
        return
    fi

    echo "Requesting vanity domain $vanity..."
    cloud domain:create "$app" --name="$vanity" --wildcard-enabled=false --json -n >/dev/null || true
}

ship_app() {
    local app="$1"
    local repo="$HERD_ROOT/$app"

    if [[ ! -d "$repo" ]]; then
        echo "Missing repo $repo" >&2
        exit 1
    fi

    cd "$repo"

    if ! cloud application:get "$app" --json -n >/dev/null 2>&1; then
        echo "Shipping $app to Laravel Cloud ($CLOUD_REGION)..."
        cloud ship -n --name="$app" --region="$CLOUD_REGION"
    else
        echo "$app already exists in Laravel Cloud"
    fi

    attach_database "$app"
    ensure_vanity_domain "$app"

    if [[ ! -f .cloud/config.json ]]; then
        cloud repo:config -n
    fi

    if [[ ! -f bin/cloud ]]; then
        mkdir -p bin
        cat >bin/cloud <<'EOF'
#!/usr/bin/env bash
export PATH="${HOME}/.composer/vendor/bin:${PATH}"
exec cloud "$@"
EOF
        chmod +x bin/cloud
    fi

    load_shared_secrets
    sync_app "$app"
    set_build_commands "$app"

    echo "Deploying $app..."
    cloud deploy "$app" "$CLOUD_ENV" -n
    cloud deploy:monitor -n

    echo "Done: $app -> $(cloud_url_for_app "$app")"
}

usage() {
    echo "Usage: $0 <app>|--all|--suite-urls|--bootstrap|--ship <app>|--ship-all" >&2
    exit 1
}

main() {
    [[ $# -ge 1 ]] || usage

    case "$1" in
        --bootstrap)
            bootstrap_from_smartcops
            ;;
        --suite-urls)
            load_shared_secrets
            for app in "${SUITE_APPS[@]}"; do
                sync_app "$app" suite-urls
            done
            ;;
        --all)
            load_shared_secrets
            for app in "${SUITE_APPS[@]}"; do
                sync_app "$app"
            done
            ;;
        --ship)
            [[ $# -eq 2 ]] || usage
            load_shared_secrets
            ship_app "$2"
            ;;
        --ship-all)
            load_shared_secrets
            for app in "${SUITE_APPS[@]}"; do
                [[ "$app" == "smartcops" ]] && continue
                ship_app "$app"
            done
            ;;
        --help|-h)
            usage
            ;;
        *)
            load_shared_secrets
            sync_app "$1"
            ;;
    esac
}

main "$@"
